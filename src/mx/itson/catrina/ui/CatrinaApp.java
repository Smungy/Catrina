    private void btnLoadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoadActionPerformed
        // TODO add your handling code here:
        
        try{
        JFileChooser fileChooser = new JFileChooser();
        
        fileChooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        
        if(fileChooser.showOpenDialog(this)== JFileChooser.APPROVE_OPTION){
            File file = fileChooser.getSelectedFile();
  
            byte fileBytes[] = Files.readAllBytes(file.toPath());
            String contenido = new String(fileBytes, StandardCharsets.UTF_8); 
           
            AccountStatement accountStatement = new AccountStatement().deserialize(contenido);
            
            List<Transaction> transactions = accountStatement.getTransactions();
            // Ordenar las transacciones por fecha (formato "AAAA-MM-DD")
            Collections.sort(transactions, (f1, f2) -> f1.getDate().compareTo(f2.getDate()));
                
             if(accountStatement !=null){
                 DefaultTableModel model =
                        (DefaultTableModel) tblTransactions.getModel();
                
                model.setRowCount(0);
                    
                float subTotal = 0;
                float totalDeposits = 0;
                float totalWithdrawals = 0 ;
                
                
             for (Transaction t : accountStatement.getTransactions()) {
        
                 float amount = (float) t.getAmount();
       
              if (t.getType() == mx.itson.catrina.enums.Type.DEPOSITS) {
            
                 subTotal += amount;
                 totalDeposits += amount;
            
               } else if (t.getType() == mx.itson.catrina.enums.Type.WITHDRAWALS) {
           
                 subTotal -= amount;
                 totalWithdrawals += amount;
            
               } else {
            
        }
              

        model.addRow(new Object[] {
            t.getDate(),
            t.getDescription(),
            (t.getType() == mx.itson.catrina.enums.Type.DEPOSITS) ? "$"+amount : "0",  
            (t.getType() == mx.itson.catrina.enums.Type.WITHDRAWALS) ? "$"+ amount : "0",  
            subTotal  
        });
                }
                 txtfinalBalance.setText(String.valueOf("$"+subTotal));
                 txtfinalBalancePS.setText(String.valueOf("$"+subTotal));
                 txtDeposits.setText(String.valueOf("$"+totalDeposits));
                 txtWithdrawals.setText(String.valueOf("$"+totalWithdrawals));
                 txtAccount.setText(accountStatement.getAccount());
                 txtClabe.setText(accountStatement.getClabe());
                 txtCurrency.setText(accountStatement.getCurrency());
                 
                 Customer customer = accountStatement.getCustomer();
                 txtId.setText(customer.getId());
                 txtName.setText(customer.getName());
                 txtRFC.setText(customer.getRfc());
                 txtAddress.setText(customer.getAddress());
                 txtCity.setText(customer.getCity());
                 txtzipCode.setText(customer.getZipCode());
             
                }
            }
        
        }catch(Exception ex){
                System.out.println("OcurriÃ³ un error: " + ex.getMessage());
                        
        }               
        
    }//GEN-LAST:event_btnLoadActionPerformed

